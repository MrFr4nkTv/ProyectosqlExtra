    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Boolean guardar = true; //Se usa para verificar si el registro se puede guardar o no
        String mensaje = ""; // Utilizada para mensajes de error
        
        String nombre = txtNombre.getText(); // Se pasa el nombre del jtextfield a una variable string
        int edad = Integer.parseInt(spnEdad.getValue().toString()); // Se convierte el jspinner a un numero entero
        String sexo = cmbSexo.getSelectedItem().toString(); // El sexo se toma del elemento seleccionado del JComboBox
        // Se da un mensaje de error si la longitud del nombre es cero
        if(nombre.length()==0){
            guardar = false;
            mensaje+="Escriba el nombre, por favor\n";
}        // Se da un mensaje de error si la edad es cero
        if(edad==0){
            guardar = false;
            mensaje+="Escriba la edad, por favor\n";
}       // En caso de que no haya ningun error se crea la instancia de control de datos, se dan los valores a la variable sexo y se regresan los objetos a sus valores inicales
        if(guardar){
            ControlDatos control = new ControlDatos();
        if(sexo.equals("Femenino")) sexo = "F";
        if(sexo.equals("Masculino")) sexo = "M";
            control.insertar(nombre, edad, sexo);
            txtNombre.setText("");
            spnEdad.setValue(0);
            cmbSexo.setSelectedIndex(0);
        llenarTabla();
}
else JOptionPane.showMessageDialog(null, mensaje); //Se notifican los errores
    }//GEN-LAST:event_btnGuardarActionPerformed

