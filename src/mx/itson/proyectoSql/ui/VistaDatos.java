    private void llenarTabla() {	
    DefaultTableModel tabla =  new DefaultTableModel();	// Se crea un DefaultTableModel debido a que es necesario debido a que la tabla fue reseteada
    ControlDatos control = new ControlDatos(); // Se crea una instancia del controlador de los datos.
    ArrayList<ModeloDatos> datos; // Arraylist del tipo modelodatos para recibir la lista del controlador
    String[] fila = new String[4]; // Array de 4 posiciones para cada fila de datos
    tabla.addColumn("ID");
    tabla.addColumn("Nombre");
    tabla.addColumn("Edad");
    tabla.addColumn("Sexo");
    datos = control.llenarDatos(); // se llena el arraylist con el metodo llenardatos
    for(int f = 0; f < datos.size(); f++){ //Ciclo for para asegurarnos de recorrer todo el ArrayList hasta su último elemento 
    fila[0] = datos.get(f).getId().toString();
    fila[1] = datos.get(f).getNombre();
    fila[2] = datos.get(f).getEdad().toString();
    fila[3] = datos.get(f).getSexo();
    tabla.addRow(fila); //Una vez completado el ciclo lo añadimos al DefaultTableModel
    }	
    tblDatos.setModel(tabla); // Hacemos que que nuestro JTable utilice como modelo nuestro DefaultTableModel.
    
    }
